#While Loop
i = 1
while i<=5:
    print(i)
    i = i + 1
print(" ")#Just to add empty line in output
#Pattern Printing 
i = 1
while i<=5:
    print(i * "*") # This works as concatenation as we multiply Strings and number it will print the string as may times the number is there
    i = i + 1
print (" ") #Just to add empty line in output
#Inverse Star Printing
i = 5
while i>=0:
    print(i * "*")
    i = i - 1
#For Loop
for i in range (5):
    print(i) 
